from typing import TypedDict


class ClinicalAgentState(TypedDict, total=False):

    # =========================================
    # USER INPUT
    # =========================================

    clinical_case: str

    # =========================================
    # ROUTING
    # =========================================

    intent: str

    clinical_domain: str

    # =========================================
    # SAFETY
    # =========================================

    safety: dict

    # =========================================
    # RAG
    # =========================================

    evidence: list[str]

    sources: list[str]

    # =========================================
    # LLM
    # =========================================

    prompt: str

    response: str

    # =========================================
    # OUTPUT SAFETY
    # =========================================

    output_safe: bool

    safety_message: str

    # =========================================
    # WORKFLOW
    # =========================================

    workflow_status: str